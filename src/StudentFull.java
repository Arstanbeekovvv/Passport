public class StudentFull {
    String name, nationality, dateOfBirth;
    Long phoneNumber;

    public StudentFull(){}
    public StudentFull(String name, String nationality, String dateOfBirth , Long phoneNumber){
        this.name = name;
        this.nationality = nationality;
        this.dateOfBirth = dateOfBirth;
        this.phoneNumber = phoneNumber;
    }

}

